    @torch.no_grad
    def forward_cache_update_vae(
        self,
        vae_model,
        past_key_values: NaiveCache,
        padded_images: torch.Tensor,
        patchified_vae_latent_shapes: List,
        packed_vae_position_ids: torch.LongTensor,
        packed_timesteps: torch.Tensor,
        packed_vae_token_indexes: torch.LongTensor,
        packed_text_ids: torch.LongTensor,
        packed_text_indexes: torch.LongTensor,
        packed_position_ids: torch.LongTensor,
        packed_seqlens: torch.IntTensor,
        packed_indexes: torch.LongTensor,
        key_values_lens: torch.IntTensor,
        packed_key_value_indexes: torch.Tensor,
        ray_plucker: torch.Tensor = None,
        masked_condition_image: torch.Tensor = None,
    ):
        packed_text_embedding = self.language_model.model.embed_tokens(packed_text_ids)
        packed_sequence = packed_text_embedding.new_zeros((sum(packed_seqlens), self.hidden_size))
        packed_sequence[packed_text_indexes] = packed_text_embedding

        padded_latent = vae_model.encode(padded_images)

        p = self.latent_patch_size
        packed_latent = list()
        for latent, (h, w) in zip(padded_latent, patchified_vae_latent_shapes):
            latent = latent[:, :h * p, :w * p].reshape(self.latent_channel, h, p, w, p)
            latent = torch.einsum("chpwq->hwpqc", latent).reshape(-1, p * p * self.latent_channel)
            packed_latent.append(latent)
        packed_latent = torch.cat(packed_latent, dim=0)
        packed_pos_embed = self.latent_pos_embed(packed_vae_position_ids)
        packed_timestep_embeds = self.time_embedder(packed_timesteps)
        
        if masked_condition_image is not None:
            # packed_latent = self.vae2llm(packed_latent) + packed_timestep_embeds + packed_pos_embed + self.vae2llm2(masked_condition_image)
            # print(self.vae2llm_zero(masked_condition_image).mean().item())
            packed_latent = self.vae2llm2(torch.cat([
                packed_latent, masked_condition_image
            ], dim=-1)) / 2 + packed_timestep_embeds + packed_pos_embed
        else:
            # packed_latent = self.vae2llm(packed_latent) + packed_timestep_embeds + packed_pos_embed + self.vae2llm2(torch.zeros_like(packed_latent))
            packed_latent = self.vae2llm2(torch.cat([
                packed_latent, torch.zeros_like(packed_latent)
            ], dim=-1)) / 2 + packed_timestep_embeds + packed_pos_embed

        if ray_plucker is not None:
            ray_embedding = F.relu(self.patch_ray(ray_plucker.permute(0, 3, 1, 2).contiguous()))
            ray_embeddings = []
            for ray_latent, (h, w) in zip(ray_embedding, patchified_vae_latent_shapes):
                ray_latent = ray_latent[:, :h * p, :w * p].reshape(self.latent_channel, h, p, w, p)
                ray_latent = torch.einsum("chpwq->hwpqc", ray_latent).reshape(-1, p * p * self.latent_channel)
                ray_embeddings.append(ray_latent)
            ray_embedding = torch.cat(ray_embeddings, dim=0)
            ray_embedding = self.ray2llm(ray_embedding)

            packed_latent = packed_latent + ray_embedding

        if packed_latent.dtype != packed_sequence.dtype:
            packed_latent = packed_latent.to(packed_sequence.dtype)
        packed_sequence[packed_vae_token_indexes] = packed_latent

        extra_inputs = {}
        if self.use_moe:
            extra_inputs = {
                "mode": "gen",
                "packed_vae_token_indexes": packed_vae_token_indexes,
                "packed_text_indexes": packed_text_indexes
            }

        output = self.language_model.forward_inference(
            packed_query_sequence=packed_sequence,
            query_lens=packed_seqlens,
            packed_query_position_ids=packed_position_ids,
            packed_query_indexes=packed_indexes,
            past_key_values=past_key_values,
            key_values_lens=key_values_lens,
            packed_key_value_indexes=packed_key_value_indexes,
            update_past_key_values=True,
            is_causal=False,
            **extra_inputs,
        )
        past_key_values = output.past_key_values

        return past_key_values

